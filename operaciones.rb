# El remainder método calcula y devuelve el resto de una operación de division de enteros:
# irb => 
16.remainder(5)
=> 1

# Tambien hay un divmod método que calcula tanto el resultado entero de la division como su valor modulo:
# irb =>
16.divmod(5)
=> \[3, 1\]

